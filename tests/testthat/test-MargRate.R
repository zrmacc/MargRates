test_that("MargRate returns valid structure and weights sum to 1.", {
  y0 <- c(10, 20)
  n0 <- c(100, 200)
  y1 <- c(15, 25)
  n1 <- c(100, 200)
  out <- MargRates:::MargRate(y0, n0, y1, n1)
  expect_type(out, "list")
  expect_named(out, c("p0", "p1", "r0", "r1", "weights"))
  expect_length(out$weights, 2L)
  expect_equal(sum(out$weights), 1)
  expect_true(all(out$p0 >= 0 & out$p0 <= 1))
  expect_true(all(out$p1 >= 0 & out$p1 <= 1))
})

test_that("MargRate with custom weights respects them.", {
  y0 <- c(10, 20)
  n0 <- c(100, 200)
  y1 <- c(15, 25)
  n1 <- c(100, 200)
  w <- c(0.5, 0.5)
  out <- MargRates:::MargRate(y0, n0, y1, n1, weights = w)
  expect_equal(out$weights, w)
  expect_equal(out$p0, 0.5 * (10/100) + 0.5 * (20/200))
  expect_equal(out$p1, 0.5 * (15/100) + 0.5 * (25/200))
})
